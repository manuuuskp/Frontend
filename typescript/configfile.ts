// include - mention the files to be included.
// exclude - mention the files/folder to be excluded.

// compiler options:
// target - sets the target version of the javascript.

// lib - by default we will have dom, dom.iterator libraries.
// we can also add extra libraries if we want.

// jsx - is for React.

// allowJS, checkJS -  will check the .js files as well and provides some of its features.

// sourceMap - will create .map.js extension files and thus making .ts files available in the browser.

// outDir - will produce the output .js files in that folder.

// rootDir - will take that as root dir to get the .ts files.

// removeComments -  will remove comments when it gets converted to .js files.

// noEmitOnError - will not let .ts compiler to create js files if .ts file has an error.

// Strict mode:

// noImplicitAny -  will not let the usage of implicit any type.
// strictNullChecks - usage of !. this will check if the element is present in the DOM. 
// so it will raise an error.
// strictBindCallApply - will check for bind, call, apply to be strict.

// Below - all the three will not let use of unused variables, parameters and strict returns.
// noUnusedLocals
// noUnusedParameters
// noImplicitReturns